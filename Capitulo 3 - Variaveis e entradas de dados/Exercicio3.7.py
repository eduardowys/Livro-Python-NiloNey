# Faça um programa que peça dois números inteiros. Imprima a soma desses dois números na tela
num1 = int(input("Digite o primeiro número inteiro: "))  # Entrada do primeiro número inteiro
num2 = int(input("Digite o segundo número inteiro: "))  # Entrada do segundo número inteiro
soma = num1 + num2  # Calcula a soma dos dois números
print(f"A soma dos números é:{soma}")  # Exibe a soma na tela    